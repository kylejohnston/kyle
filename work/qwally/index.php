				<div class="inner">
					<h2>UX: Quick Start Plan</h2>
					<p>Based on the user flow mapping, feedback from city officials, and Qwally’s user testing, we created a quiz-like experience for business owners that quickly leads them through a series of relevant questions.</p>
					<p>After completing the quiz, users receive a personalized checklist they can use to ensure they have everything they need to be eligible to work with the city.</p>
					<p>The final UX and design present a series of simple questions that business owners can complete in minutes, a huge improvement over the previous process, which required lengthy in-person meetings.</p>
